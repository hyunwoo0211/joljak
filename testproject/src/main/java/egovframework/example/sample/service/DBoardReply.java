package egovframework.example.sample.service;

public class DBoardReply {

	private String take_reply_id;
	private String board_take_id;
	private String user_id;
	private String take_reply_date;
	private String take_reply_content;
	
	
	public String getTake_reply_id() {
		return take_reply_id;
	}
	public void setTake_reply_id(String take_reply_id) {
		this.take_reply_id = take_reply_id;
	}
	public String getBoard_take_id() {
		return board_take_id;
	}
	public void setBoard_take_id(String board_take_id) {
		this.board_take_id = board_take_id;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getTake_reply_date() {
		return take_reply_date;
	}
	public void setTake_reply_date(String take_reply_date) {
		this.take_reply_date = take_reply_date;
	}
	public String getTake_reply_content() {
		return take_reply_content;
	}
	public void setTake_reply_content(String take_reply_content) {
		this.take_reply_content = take_reply_content;
	}
	
	
}
